public class LongValue {

    private long value;

    public LongValue(long value) {
        this.value = value;
    }

    /**
     * @return the value
     */
    public long getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(long value) {
        this.value = value;
    }

}
